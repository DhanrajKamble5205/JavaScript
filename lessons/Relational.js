// Relational operator

// > - more than
// < - Less than
// >= - more than equal
// <= - Less than equal

var result = 10 > 5
console.log(result)

var result = 4 > 5
console.log(result)


// Equality Operator

var x = 5
console.log(x == '5') // true. here it first convert the type of value then check it is same or not
console.log(x === '5') // false. without converting type it check value is same or not
console.log(x === 5) // true. because type of the comparison is same.